<h2>Heart Of Darkness</h2>
<p>By: Joseph Conrad<br>
Published: 1899<br>
Set: In the days of Colonialism, on the Congo</p>

<p>The story begins with Marlow and some other sailors on a boat. The Thames is flooded and they cannot leave port. Marlow recounts his trip into the heart of darkness.</p>

<p>By recommendation of his aunt, Marlow is given a job by the Belgians, to pilot steamers down the Congo. He travels to Brussels to sign his contract.</p>

<p>Marlow is then taken by steamer to his company's station in Matadi. He stays ten days. He is informed of Mr. Kurtz and how Mr. Kurtz will be promoted to an administration position shortly.</p>

<p>Marlow travel with some caravans 200 miles to central station. Along the way, he must carry one of his companions, who is injured.</p>

<p>When he arrives at central statio, Marlow discovers that his steamer is wrecked. He requires rivets to fix it, which do not arrive for 3 months. He later learns that this is because the manager of central station wants Kurtz to die so he can be promoted instead.</p>

<p>The station is “invaded” by the Eldorado Exploring Expedition. The leader of the Expedition is the uncle of the manager of central station. Marlow overhears them plotting for the manager to outlast Kurtz.</p>

<p>Marlow set off down the river. He states that for him, the Congo leads to Kurtz, and travelling down the river is like going back in time. They watch some rituals and react "as sane men would be before an enthusiastic outbreak in a madhouse."</p>

<p>50 miles for central station they find a woodpile with a note and a book about sailing. The note reads, “Wood for you. Hurry up. Approach cautiously.”</p>

<p>They continue on, and stop for a night. In the morning the boat is surrounded by fog. They are then attacked by natives. They lose the fireman. Marlow sounds the whistle which scares them off.</p>

<p>Marlow throws the fireman overboard, much to the disappointment of his cannibalistic kinsmen.</p>

<p>Shortly after, they arrive at Kurtz's station. Marlow finally gets to meet the legendary figure. The station is not much, has many severed heads and Kurtz is a sick, hollow shell. Most information about Kurtz is acquired from an extreme fan of Kurtz, who is living with him. </p>

<p>Marlow finds out that Kurtz has amassed heaps of ivory. He has fasioned himself as a god for on the tribes and they obey his every word. Marlow is given Kurtz's report which is generally well meaning and which proposes many good things. However, it is amended by Kurtz' scribble : “Exterminate all the brutes!”</p>

<p>They take Kurtz and travel back to Europe. Kurtz dies. Marlow speaks with Kurtz fiancée who thinks Kurtz is a wonderful person, an extraordinary human. She asks what his last words were, and Marlow tells her that Kurtz last words were her name. </p>

