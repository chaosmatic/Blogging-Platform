<h2>Hamlet</h2>

<p>Author: Shakespeare <br>Published: 1600-5  <br>Set: Danish Royal Palace, Europe</p>

<p>The play begins with the ghost of Old Hamlet appearing to the guards. He will not speak to them. They decide to tell Hamlet about the ghost.</p>

<p>Claudius sends messengers to Norway to prevent an invasion. Laertes is granted leave by his father and the king to return to France. Hamlet is still mourning for the loss of his father. Hamlet longs for death but knows suicide is forbidden by god. He is disgusted by his mothers actions in marrying so quickly after his dad's death. Horatio tells Hamlet about the ghost. Hamlet questions him, but is still curious.</p>

<p>Laertes warns Ophelia about hamlet's love, saying that Hamlet must marry in the interests of the state. Polonius gives Laertes a speech about his behaviour and how he should conduct himself in France. He also warns Ophelia about Hamlet's love.</p>

<p>Hamlet is at the platform waiting for his fathers ghost. He remarks that some men have one bad character trait which ruins them entirely. The ghost appears. It beckons Hamlet, and he follows, much to his friends protests.</p>

<p>The ghost tells Hamlet to avenge his murder, and reveals that he was murded by Claudius. He adds that Hamlet should not harm his mother. Hamlet comes back to Horatio and Marcellus and asks them not to tell anyone. He then proclaims he will act in an odd manner to find out more about other peoples intentions.</p>

<p>Ophelia reports Hamlet's odd behaviour to her father, who thinks that hamlet is driven mad by rejected love. He then decided to tell Claudius about hamlets actions.</p>

<p>Claudius and Gertrude arrange for Rosencratz and Guildenstern, Hamlets old school friends, to spy on Hamlet. Polonius tells Claudius his ideas about Hamlet's madness. They plan to spy on Hamlet in the courtyard. Hamlet talks to Rosencratz and Guildenstern, and determines that they are spying on him. The travelling player arrive. Hamlet organises to determine Claudius' guilt by having the players stage a show about a brother who murders a king. He is annoyed with himself as actor can cry over nothing but he is inactive about his fathers murder.</p>

<p>Claudius and Polonius spy on Hamlet and his meeting with Ophelia. Hamlet makes his well known speech (To be or not to be) about mortality, and scorns Ophelia. Polonius and Claudius plan to have Hamlet killed in England, and for Polonius to spy on Hamlet's meeting with his mother.</p>

<p>Hamlet prepares the players, and tells Horatio to watch for Claudius to see his guild. Hamlet again scorns and teases Ophelia. Halfway through the play Claudius leaves and Horatio and hamlet discuss his guilt.</p>

<p>Claudius prays and acknowledges his guilt. However, Hamlet refrains from killing him so that he can kill him at a time when Claudius' soul will go straight to hell.</p>

<p>Hamlet berates his mother. Polonius moves behind the curtain, and Hamlet, suspecting Claudius, kills him. He then continues to berate his mother until the ghost appears, warning Hamlet of his mission. Claudius decides to send Hamlet away, and tells Rosencratz and Guildenstern to look for Polonius' body.</p>

<p>Hamlet finally reveals the location of the body, and is sent to England to be killed by the King of England. They pass the Norwegian army about to invade Poland for a useless piece of land. Hamlet thinks about how bad society.</p>

<p>Ophelia is made, sand and insane. Laertes tries to usurp the throne but Claudius consoles him, saying he is innocent and will help avenge Polonius' death. </p>

<p>Claudius talks with Laertes about why he didn't punish Hamlet for his murder of Polonius. It is revealed to them that Hamlet is coming back, so they plan a duel between Laertes and Hamlet where Laertes will use a poisoned sword and Claudius will provide Hamlet with a poisoned drink. Gertrude reveals that Ophelia accidentally drowned.</p>

<p>Hamlet visits the graveside of Ophelia. She is getting a Christian burial even though the circumstances are questionable, Hamlet reflect son death and how it is the great leveller. Hamlet and Laertes fight over who loved Ophelia more.</p>

<p>Hamlet tells Horatio of how he sent Rosencratz and Guildenstern to their deaths and is not remorseful. He argues that he is justified in killing Claudius. Orsic informs him of the wager on the duel. Hamlet accepts to fight in the duel and it takes place. Before the fight, Hamlet apologises for his actions with regards to Ophelia and Polonius. The Queen drinks the poison accidentally. Hamlet and Laertes fatally wound each other, and Hamlet kills Claudius. It is announced that Rosencratz and Guildenstern are dead. Fortinbras claims the Throne of Denmark.</p>

